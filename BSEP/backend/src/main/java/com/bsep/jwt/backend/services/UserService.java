    public UserDto register(SignUpDto userDto) {
        Optional<User> optionalUser = userRepository.findByLogin(userDto.login());

        if (optionalUser.isPresent()) {
            throw new AppException("Login already exists", HttpStatus.BAD_REQUEST);
        }
        if (!isPasswordValid(userDto.password())) {
            throw new AppException("Password does not meet complexity requirements", HttpStatus.BAD_REQUEST);
        }

        User user = userMapper.signUpToUser(userDto);
        user.setRole(Role.CLIENT);
        user.setRegistrationStatus(RegistrationStatus.PENDING);
        String salt = generateSalt();
        user.setSalt(salt);
        String pass = Arrays.toString(userDto.password()) +salt;
        user.setPassword(passwordEncoder.encode(CharBuffer.wrap(pass)));
        //user.setPassword(passwordEncoder.encode(CharBuffer.wrap(userDto.password())));
        user.setLegal(userDto.isLegal());
        if(userDto.packageType().equals("BASIC")){
            user.setPackageType(PackageType.BASIC);
        } else if(userDto.packageType().equals("STANDARD")){
            user.setPackageType(PackageType.STANDARD);
        } else {
            user.setPackageType(PackageType.GOLD);
        }
        if(userDto.pib()!=null){
            user.setPib(userDto.pib());
        }
        if(userDto.companyName()!=null){
            user.setCompanyName(userDto.companyName());
        }
        if(userDto.address()!=null){
            user.setAddress(userDto.address());
        }
        if(userDto.city()!=null){
            user.setCity(userDto.city());
        }
        if(userDto.country()!=null){
            user.setCountry(userDto.country());
        }
        if(userDto.phoneNumber()!=null){
            user.setPhoneNumber(userDto.phoneNumber());
        }
        User savedUser = userRepository.save(user);
        return userMapper.toUserDto(savedUser);
    }

