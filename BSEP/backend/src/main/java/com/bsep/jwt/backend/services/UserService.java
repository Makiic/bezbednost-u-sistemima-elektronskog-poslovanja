    public void confirmRegistration(Integer userId){
        User user = userRepository.findById(userId);
        String token = UUID.randomUUID().toString();
        ConfirmationToken confirmationToken = new ConfirmationToken(
                token,
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(15),
                user
        );
        confirmationTokenService.saveConfirmationToken(confirmationToken);

        String link = "http://localhost:8080/api/users/confirm?token=" + token;
        emailSender.send(user.getLogin(),buildEmail(user.getFirstName(),link ));
        userRepository.updateUserRegistrationStatusToConfirmed(userId);
    }
