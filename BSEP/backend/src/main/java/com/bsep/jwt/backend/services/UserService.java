    public boolean isPasswordValid(char[] password) {
        // Pretvorimo char[] u String radi lakše manipulacije
        String passwordString = new String(password);

        // Provera dužine lozinke
        if (password.length < 8) {
            return false;
        }

        // Provera da li lozinka sadrži barem jedan broj
        if (!containsNumber(passwordString)) {
            return false;
        }

        // Provera da li lozinka sadrži barem jedan specijalni karakter
        if (!containsSpecialCharacter(passwordString)) {
            return false;
        }

        return true;
    }

    private boolean containsNumber(String password) {
        return Pattern.compile("\\d").matcher(password).find();
    }

    private boolean containsSpecialCharacter(String password) {
        return !Pattern.matches("[A-Za-z0-9]*", password);
    }
    public static String generateSalt() {
        SecureRandom random = new SecureRandom();
        byte[] salt = new byte[16];
        random.nextBytes(salt);
        return Base64.getEncoder().encodeToString(salt);
    }

    public static String generateOneTimePassword() {
        SecureRandom random = new SecureRandom();
        StringBuilder otp = new StringBuilder();
        for (int i = 0; i < 6; i++) {
            otp.append(random.nextInt(10));
        }
        return otp.toString();
    }

